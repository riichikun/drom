declare(strict_types=1);

namespace BaksDev\Drom\UseCase\Admin\NewEdit;

use BaksDev\Drom\UseCase\Admin\NewEdit\Active\DromTokenActiveForm;
use BaksDev\Drom\UseCase\Admin\NewEdit\Key\DromTokenKeyForm;
use BaksDev\Drom\UseCase\Admin\NewEdit\Kit\DromTokenKitDTO;
use BaksDev\Drom\UseCase\Admin\NewEdit\Kit\DromTokenKitForm;
use BaksDev\Drom\UseCase\Admin\NewEdit\Percent\DromTokenPercentForm;
use BaksDev\Drom\UseCase\Admin\NewEdit\Profile\DromTokenProfileForm;
use Symfony\Component\Form\AbstractType;
use Symfony\Component\Form\Extension\Core\Type\CollectionType;
use Symfony\Component\Form\Extension\Core\Type\SubmitType;
use Symfony\Component\Form\FormBuilderInterface;
use Symfony\Component\Form\FormEvent;
use Symfony\Component\Form\FormEvents;
use Symfony\Component\OptionsResolver\OptionsResolver;

final class DromTokenNewEditForm extends AbstractType
{
    public function buildForm(FormBuilderInterface $builder, array $options): void
    {

        $builder->add('profile', DromTokenProfileForm::class, ['label' => false]);

        $builder->add('key', DromTokenKeyForm::class, ['label' => false]);

        $builder->add('active', DromTokenActiveForm::class, ['label' => false]);

        $builder->add('percent', DromTokenPercentForm::class, ['label' => false]);

        $builder->add('drom_token', SubmitType::class, [
            'label' => 'Save',
            'label_html' => true,
            'attr' => ['class' => 'btn-primary'],
        ]);

        $builder->addEventListener(
            eventName: FormEvents::PRE_SET_DATA,
            listener: function(FormEvent $event) use ($options) {

                $form = $event->getForm();

                $dromTokenNewEditDTO = $event->getData();

                if($dromTokenNewEditDTO instanceof DromTokenNewEditDTO)
                {
                    if(true === $dromTokenNewEditDTO->getKit()->isEmpty())
                    {
                        $dromTokenNewEditDTO->addKit(new DromTokenKitDTO);
                    }
                }

                $form->add('kit', CollectionType::class, [
                    'entry_type' => DromTokenKitForm::class,
                    'entry_options' => [
                        'label' => false,
                    ],
                    'label' => false,
                    'by_reference' => false,
                    'allow_delete' => true,
                    'allow_add' => true,
                    'prototype_name' => '__kit__',
                ]);

            });
    }

    public function configureOptions(OptionsResolver $resolver): void
    {
        $resolver->setDefaults([
            'data_class' => DromTokenNewEditDTO::class,
            'method' => 'POST',
            'attr' => ['class' => 'w-100'],
        ]);
    }
}
